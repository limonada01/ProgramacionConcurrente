package CarmposFuentes_FAI2438.Comedor3;



public class Animal  {
    private int id;
    private int especie;
    private C0medor comedor;
    public Animal(int id,int especie, C0medor comedor){
        this.id=id;
        this.especie=especie;
        this.comedor=comedor;

    }

    public int getId() {
        return id;
    }

   

    public int getEspecie() {
        return especie;
    }

  

    public C0medor getComedor() {
        return comedor;
    }

    

    
}
